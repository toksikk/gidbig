func isEmojiModifier(r rune) bool {
	return r == 0xFE0F || // variation selector-16 (emoji presentation)
		r == 0xFE0E || // variation selector-15 (text presentation)
		r == 0x200D || // zero-width joiner
		r == 0x20E3 || // combining enclosing keycap
		(r >= 0x1F3FB && r <= 0x1F3FF) // skin tone modifiers
}

// isValidBeverageEmoji reports whether s is acceptable as a beverage emoji.
// It accepts a single Unicode emoji (including compound sequences with modifiers)
// or a Discord custom emoji reference (<:name:id> or <a:name:id>).
func isValidBeverageEmoji(s string) bool {
	if len(s) == 0 {
		return false
	}
	if customEmojiRe.MatchString(s) {
		return true
	}
	hasEmoji := false
	for _, r := range s {
		if unicode.Is(emojiRanges, r) {
			hasEmoji = true
		} else if !isEmojiModifier(r) {
			return false
		}
	}
	return hasEmoji
}
